    private void bttn_filtrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttn_filtrarActionPerformed
        // if todos iguais a "--" a pokedex tira o filtro
        if(tipo_box.getSelectedItem().equals("--") && cor_box.getSelectedItem().equals("--") && formato_box.getSelectedItem().equals("--")){
            adicionarTabelaPokedex();
        }else{
            DefaultTableModel modelo = new DefaultTableModel(new Object[] {"Numero", "Nome", "Tipo1", "Tipo2", "Cor", "Formato"}, 0);
            String query = "SELECT numero,nome,tipo_1,tipo_2,cor,formato FROM pokedex_tabela WHERE";
            if (!(cor_box.getSelectedItem().equals("--"))){
                query = query + " cor = '"+cor_box.getSelectedItem()+"' AND";
            }
            
            if (!(tipo_box.getSelectedItem().equals("--"))){
                query = query + " (tipo_1 = '"+tipo_box.getSelectedItem()+"' OR tipo_2 = '"+tipo_box.getSelectedItem()+"') AND";
            } 
            
            if (!(formato_box.getSelectedItem().equals("--"))){
                query = query + " formato = '"+formato_box.getSelectedItem()+"' AND";
            } 
            query = query.substring(0, query.length()-4) + ";";
            try{
                var statement = conn.createStatement();
                var result = statement.executeQuery(query);
                while (result.next()){
                    Object linha[];
                    /// adicionar nos {} todas as informações do objeto acima.
                    if (result.getString("tipo_2").equals("none")){
                        linha = new Object[]{result.getString("numero"),result.getString("nome"),result.getString("tipo_1"),"",result.getString("cor"),result.getString("formato")};
                    } else{
                        linha = new Object[]{result.getString("numero"),result.getString("nome"),result.getString("tipo_1"),result.getString("tipo_2"),result.getString("cor"),result.getString("formato")};
                    }
                    modelo.addRow(linha);
                    }
            } catch (SQLException ex){
                System.err.println("filtrar deu ruim:"+ex.getMessage());
            }
            
            pokeTabela.setModel(modelo);
        }
    }//GEN-LAST:event_bttn_filtrarActionPerformed

