    public void adicionarTabelaPokedex(){
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"Numero", "Nome", "Tipo1", "Tipo2", "Cor", "Formato"}, 0);
        //TODO: fazer aqui. iterar por todo pokemon. nova view pra isso
        String query = "SELECT numero,nome,tipo_1,tipo_2,cor,formato FROM pokedex_tabela;";
        try{
            var cursor = conn.createStatement();
            var retorno = cursor.executeQuery(query);
            while (retorno.next()){
                Object linha[];
                /// adicionar nos {} todas as informações do objeto acima.
                if (retorno.getString("tipo_2").equals("none")){
                    linha = new Object[]{retorno.getString("numero"),retorno.getString("nome"),retorno.getString("tipo_1"),"",retorno.getString("cor"),retorno.getString("formato")};
                } else{
                    linha = new Object[]{retorno.getString("numero"),retorno.getString("nome"),retorno.getString("tipo_1"),retorno.getString("tipo_2"),retorno.getString("cor"),retorno.getString("formato")};
                }
                modelo.addRow(linha);
            }
        }catch (SQLException ex){
                System.err.println(ex.getMessage());
        }
        
        pokeTabela.setModel(modelo);
    }

