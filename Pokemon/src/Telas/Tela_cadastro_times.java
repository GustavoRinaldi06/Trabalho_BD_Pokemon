     public void CarregarTimes(){
        times_box.removeAllItems();
        times_box.addItem("--");
        String query = "SELECT nome FROM equipes WHERE nome_dono = session_user;";
        try{
            var cursor = conn.createStatement();
            var retorno = cursor.executeQuery(query);
            while (retorno.next()){
                times_box.addItem(retorno.getString("nome"));;
            }
        } catch (SQLException ex){
            System.err.println(ex.getMessage());
        }
    }
