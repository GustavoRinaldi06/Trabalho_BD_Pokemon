    private void bttn_cria_timeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttn_cria_timeActionPerformed
        if(txt_nome_time.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Escoha o nome para o seu time!", "Muitos pokemons... separe por nomes!", JOptionPane.ERROR_MESSAGE);
        }else{
            // cria o time e abre a tela do time
            // procedure insere_time
            String comando;
            if (status_box.getSelectedItem().equals("privado")){
                comando = "CALL insere_time('"+txt_nome_time.getText()+"',true);";
            } else {
                comando = "CALL insere_time('"+txt_nome_time.getText()+"',false);";
            }
            try{
                var cursor = conn.createStatement();
                var retorno = cursor.execute(comando);
                new Meu_time(conn,txt_nome_time.getText()).setVisible(true);
                CarregarTimes();
                CarregarStatus();
                txt_nome_time.setEnabled(false);
                txt_nome_time.setText("");
                bttn_cria_time.setEnabled(false);
                times_box.setEnabled(true);
                bttn_confirma.setEnabled(true);
                
            } catch (SQLException ex){
                System.err.println(ex.getMessage());
            }
            
        }
    }//GEN-LAST:event_bttn_cria_timeActionPerformed

