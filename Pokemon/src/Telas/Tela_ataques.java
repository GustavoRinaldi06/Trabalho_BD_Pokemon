    public void CarregarTipos(){
        tipo_filter.removeAllItems();
        tipo_filter.addItem("--");
        String query = "SELECT nome FROM tipos;";
        try{
            var cursor = conn.createStatement();
            var retorno = cursor.executeQuery(query);
            while (retorno.next()){
                tipo_filter.addItem(retorno.getString("nome"));
            }
        } catch (SQLException ex){
            System.err.println(ex.getMessage());
        }
    }
    public void adicionarTabelaAtaques(){
        DefaultTableModel modelo = new DefaultTableModel(new Object[] {"Nome", "Tipo", "Poder", "Precisão"}, 0);
        // iterar pela lista completa de ataques do pokemon
        String query = "SELECT ataques.nome as nome, ataques.nome_tipo as tipo, ataques.accuracy as precisao, ataques.power as poder  FROM ataques, aprende_ataque, pokemons WHERE pokemons.nome = '"+nome+"' AND ataques.nome = aprende_ataque.nome_ataque AND pokemons.numero = aprende_ataque.num_poke AND pokemons.forma = aprende_ataque.num_forma;";
        try{
            var stmt = conn.createStatement();
            var result = stmt.executeQuery(query);
            while (result.next()){
                Object linha[] = new Object[]{result.getString("nome"), result.getString("tipo"), result.getString("poder"), result.getString("precisao")};
                modelo.addRow(linha);
            }
        } catch (SQLException ex){
            System.err.println("adicionarTabela: "+ex.getMessage());
        }
        
        tabela_ataques.setModel(modelo);
    }
