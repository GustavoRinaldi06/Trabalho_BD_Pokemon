    private void tabela_ataquesMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabela_ataquesMouseClicked
        // pega as informações do ataque x e preenche os txt
        // ****************************
        String atk_nom = tabela_ataques.getValueAt(tabela_ataques.getSelectedRow(),0).toString();
        // a row precisa ser maior q 0 e menor oiu igual ao numero de opções
        txt_nome_ataque.setText(atk_nom);
        String query = "SELECT texto, nome_tipo FROM ataques WHERE nome = '"+atk_nom+"';";
        String tipo = "";
        String fortes = "";
        String fracos = "";
        try{
            var stmt = conn.createStatement();
            var result = stmt.executeQuery(query);
            result.next();
            txt_desc.setText(result.getString("texto"));
            tipo = result.getString("nome_tipo");
            
        } catch (SQLException ex){
            System.err.println("texto: "+ex.getMessage());
        }
        try{
            query = "SELECT defensor FROM relacao_tipos WHERE atacante = '"+tipo+"' AND multiplicador > 1;";
            var stmt = conn.createStatement();
            var result = stmt.executeQuery(query);
            if (result.next()) {
                fortes = result.getString("defensor");
                while (result.next()){
                    fortes = fortes+", "+result.getString("defensor");
                }
            }
        } catch (SQLException ex){
            System.err.println("fortes: "+ex.getMessage());
        }
        try{
            query = "SELECT defensor FROM relacao_tipos WHERE atacante = '"+tipo+"' AND multiplicador < 1;";
            var stmt = conn.createStatement();
            var result = stmt.executeQuery(query);
            result.next();
            fracos = result.getString("defensor");
            while (result.next()){
                fracos = fracos+", "+result.getString("defensor");
            }
        } catch (SQLException ex){
            System.err.println("fracos: "+ex.getMessage());
        }
        txt_forte.setText(fortes);
        txt_fraco.setText(fracos);
    }//GEN-LAST:event_tabela_ataquesMouseClicked

