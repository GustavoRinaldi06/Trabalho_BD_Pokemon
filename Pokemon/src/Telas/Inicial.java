    private void bttn_cadastroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttn_cadastroActionPerformed
        if(txt_user.getText().equals("") || txt_senha.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Insira todas as informações", "Alerta!", JOptionPane.INFORMATION_MESSAGE);
        }else{
            try {
                String comando = "CALL novo_user('"+txt_user.getText()+"','"+txt_senha.getText()+"');";
                var connection = DriverManager.getConnection("jdbc:postgresql://localhost:5432/pokemon", "anon","123");
                var statement = connection.createStatement();
                var result = statement.execute(comando);
                JOptionPane.showMessageDialog(null, "Usuário criado com sucesso", "!", JOptionPane.ERROR_MESSAGE);
            } catch (SQLException ex){
                System.err.println(ex.getMessage());
            } 
        }
    }//GEN-LAST:event_bttn_cadastroActionPerformed

