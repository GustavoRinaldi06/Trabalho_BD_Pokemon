    private void bttn_loginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bttn_loginActionPerformed
        if(txt_user.getText().equals("") || txt_senha.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Insira todas as informações", "Alerta!", JOptionPane.ERROR_MESSAGE);
        }else{

            Connection connection;
            try {
                connection = DriverManager.getConnection("jdbc:postgresql://localhost:5432/pokemon", txt_user.getText(),txt_senha.getText());
                new Pokedex(connection).setVisible(true);
                this.setVisible(false);
                new Tela_cadastro_times(connection).setVisible(true);   
            } catch (SQLException ex) {
                JOptionPane.showMessageDialog(null, "Usuário e/ou senha incorretos", "Alerta!", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_bttn_loginActionPerformed

